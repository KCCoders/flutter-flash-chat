String getFirebaseError(dynamic e) {
  try {
    if (Platform.isAndroid) {
      return e.message.toString();
    } else if (Platform.isIOS) {
      return e.code.toString();
    } else {
      return 'Platform not recognized.';
    }
  } catch (ex) {
    print(ex);
  }
}
